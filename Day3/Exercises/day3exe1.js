function add(a, b) {
  return a + b;
}
function subtract(a, b) {
  return a - b;
}
function multiply(a, b) {
  return a * b;
}
function divide(a, b) {
  return a / b;
}
console.log("Addition:", add(10, 20));
console.log("Subtraction:", subtract(30, 10));
console.log("Multiplication:", multiply(5, 6));
console.log("Division:", divide(20, 4));
